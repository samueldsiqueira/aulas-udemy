const a = 7; // aqui é atribuido o valor
let b = 3;

b += a;
/* isso significa
 que está sendo feita uma atribuição aditiva
  no caso acrescentando o valor de A a B*/
console.log(b);

b -= 4; // b = b -4
console.log(b);

b *= 2; //b = b * 2
console.log(b);

b /= 2; // b = b / 2
console.log(b);

b = b / 2
console.log(b);

b %= 2; // b = b % 2 o simbolo de %(modulo)
console.log(b);